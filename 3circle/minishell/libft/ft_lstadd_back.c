#include "libft.h"

void	ft_lstadd_back(t_list **alst, t_list *new)
{
	t_list	*start;

	if (*alst == NULL)
	{
		(*alst) = new;
		return ;
	}
	start = *alst;
	*alst = ft_lstlast(*alst);
	(*alst)->next = new;
	*alst = start;
}

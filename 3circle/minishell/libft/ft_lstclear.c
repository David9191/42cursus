#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	void	*temp;

	while ((*lst) != NULL)
	{
		temp = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = temp;
	}
	ft_gc_free(*lst);
	*lst = NULL;
}

#include "ft_printf.h"

void	hexa_print(unsigned long n, char d)
{
	char	c;

	c = n % 16;
	if (n >= 16)
		hexa_print(n / 16, d);
	if (c >= 0 && c < 10)
		c += '0';
	else if (d == 'X' && c >= 10 && c < 16)
		c = c - 10 + 'A';
	else if ((d == 'x' || d == 'p')
            && (c >= 10 && c < 16))
		c = c - 10 + 'a';
	write(1, &c, 1);
}